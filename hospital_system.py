    "        date = self.app_date.get().strip()\n",
    "        try:\n",
    "            dt = datetime.strptime(date, \"%Y-%m-%d\")\n",
    "            dow = dt.weekday()\n",
    "        except:\n",
    "            messagebox.showerror(\"Error\", \"Invalid date format\")\n",
    "            return\n",
    "        schedule = query_db(\"SELECT StartTime, EndTime, SlotDuration FROM DoctorAvailability WHERE DoctorID=? AND DayOfWeek=?\", (doc_id, dow))\n",
    "        if not schedule:\n",
    "            self.slots_list.delete(0, tk.END)\n",
    "            self.slots_list.insert(tk.END, \"Doctor not available on this day\")\n",
    "            return\n",
    "        start, end, dur = schedule[0]\n",
    "        booked = query_db(\"SELECT StartTime FROM Appointments WHERE DoctorID=? AND AppointmentDate=?\", (doc_id, date))\n",
