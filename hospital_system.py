    "        end_t = datetime.strptime(str(end), \"%H:%M:%S\")\n",
    "        while cur_t < end_t:\n",
    "            slot = cur_t.strftime(\"%H:%M\")\n",
    "            if slot not in booked_times:\n",
    "                slots.append(slot)\n",
    "            cur_t += timedelta(minutes=dur)\n",
    "        self.slots_list.delete(0, tk.END)\n",
    "        for s in slots:\n",
    "            self.slots_list.insert(tk.END, s)\n",
    "        if not slots:\n",
    "            self.slots_list.insert(tk.END, \"No available slots\")\n",
